console.log(typeof(222.00)) // tipo number
console.log(typeof(true)) // tipo bool
console.log(typeof(10 > 0)) // tipo bool
console.log(typeof(!!"")) // a dupla negação de coisa vazias tem um valor falso
console.log(typeof(!!"10")) // a dupla negação em coisas que estão não vazia, são True