/** Operador	Significado
+	Soma
–	Subtração
*	Multiplicação
/	Divisão (normal)
%	Módulo
++	Incremento
—	Decremento */
soma = 1 + 2
console.log(soma)
subtracao = 1 - 1 
console.log(subtracao)
multiplicacao = 6 * 6
console.log(multiplicacao)
divisao = 10 / 5
console.log(divisao)
modulo = 10 % 3
console.log(modulo)
incremento = 0
incremento++
incremento++
console.log(incremento)
decremento = 5
decremento--
console.log(`Isso é o resultado de incremento ${decremento}`)
